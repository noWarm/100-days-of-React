export const printBoard = (board: number[][]) => {
  let boardString = "";
  for (let i = 0; i < board.length; i++) {
    for (let j = 0; j < board[0].length; j++) {
      if (board[i][j] == EMPTY_BOARDCELL) {
        boardString += ".." + " ";
      } else {
        boardString += String(board[i][j]).padStart(2, "0") + " ";
      }
    }
    boardString += "\n";
  }
  console.log(boardString);
};
